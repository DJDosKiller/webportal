package me.stutiguias.webportal.settings;

/**
 *
 * @author Daniel
 */
public class AuctionEnchant {
    
    private int id;
    private int enchtId;
    private int itemTableId;
    private int itemId;
   
    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the enchtId
     */
    public int getEnchtId() {
        return enchtId;
    }

    /**
     * @param enchtId the enchtId to set
     */
    public void setEnchtId(int enchtId) {
        this.enchtId = enchtId;
    }

    /**
     * @return the itemTableId
     */
    public int getItemTableId() {
        return itemTableId;
    }

    /**
     * @param itemTableId the itemTableId to set
     */
    public void setItemTableId(int itemTableId) {
        this.itemTableId = itemTableId;
    }

    /**
     * @return the itemId
     */
    public int getItemId() {
        return itemId;
    }

    /**
     * @param itemId the itemId to set
     */
    public void setItemId(int itemId) {
        this.itemId = itemId;
    }
}
